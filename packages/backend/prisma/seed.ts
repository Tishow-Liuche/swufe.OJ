async function main() {
  console.log('开始导入题目 P1000-P1010...\n');

  for (const p of problems) {
    // 检查是否已存在
    const existing = await prisma.problem.findFirst({ where: { title: p.title } });
    if (existing) {
      console.log(`  [跳过] ${p.title} 已存在`);
      continue;
    }

    // 创建题目
    const problem = await prisma.problem.create({
      data: {
        title: p.title,
        source: 'LOCAL',
        status: 'PUBLISHED',
        difficulty: p.difficulty,
        timeLimit: p.timeLimit,
        memoryLimit: p.memoryLimit,
        versions: {
          create: {
            description: p.description,
            version: 1,
          },
        },
        tags: {
          create: p.tags.map((name) => ({ name, type: 'TAG' })),
        },
      },
      include: { versions: { where: { isCurrent: true }, take: 1 } },
    });

    console.log(`  [导入] ${p.title} — ${p.tags.join(', ')} — ${p.testCases.length} 个测试点`);
  }

  const count = await prisma.problem.count();
  console.log(`\n导入完成！题库共 ${count} 道题目`);
}

main()
  .catch((e) => {
    console.error('导入失败:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
