async function main() {
  console.log(`准备导入 ${PROBLEMS.length} 道题目...`);

  // 已存在的标题集合
  const existing = new Set((await p.problem.findMany({ select: { title: true } })).map(p => p.title));
  let created = 0, skipped = 0;

  for (const [title, difficulty, tags] of PROBLEMS) {
    if (existing.has(title)) { skipped++; continue; }

    const source = title.match(/^P\d+/) ? 'EXTERNAL' : 'LOCAL';
    const pid = title.match(/P\d+/)?.[0];
    const url = pid ? `https://www.luogu.com.cn/problem/${pid}` : null;

    const problem = await p.problem.create({
      data: {
        title,
        source,
        difficulty,
        timeLimit: 1000,
        memoryLimit: difficulty === 'IMPROVE' ? 256 : 125,
        status: 'PUBLISHED',
        versions: {
          create: {
            version: 1,
            description: source === 'EXTERNAL'
              ? `## 题目描述\n\n本题来自洛谷题库 **[${title}](${url})**。\n\n主要考察：${tags.join('、')}。\n\n## 输入格式\n\n请参考原题链接。\n\n## 输出格式\n\n请参考原题链接。\n\n## 提示\n\n- 难度: ${difficulty}\n- 标签: ${tags.join(', ')}`
              : `## 题目描述\n\n本平台原创入门题。\n\n## 标签\n\n${tags.join('、')}`,
          },
        },
        tags: { create: tags.map(n => ({ name: n, type: 'TAG' })) },
        sourceInfo: source === 'EXTERNAL' && url ? {
          create: { platform: 'LUOGU', remoteProblemId: pid || '', remoteUrl: url },
        } : undefined,
      },
      include: { versions: { where: { isCurrent: true }, take: 1 } },
    });

    // 自动生成简单测试用例
    const ver = problem.versions[0];
    if (ver && tags.includes('模拟') && difficulty !== 'IMPROVE') {
      await p.problemTestCase.createMany({
        data: [{ problemVersionId: ver.id, input: '', expectedOutput: '', score: 100, order: 1, isSample: true }],
      });
    }

    created++;
    if (created % 50 === 0) console.log(`  已创建 ${created} 题...`);
  }

  const total = await p.problem.count();
  console.log(`\n完成！创建 ${created} 题，跳过 ${skipped} 题，总计 ${total} 题`);
  console.log(`来源分布: LOCAL=${await p.problem.count({where:{source:'LOCAL'}})} EXTERNAL=${await p.problem.count({where:{source:'EXTERNAL'}})}`);
  await p.$disconnect();
}

main().catch(e => { console.error(e); process.exit(1); });
